                    Q4：能具体讲一讲“达芬奇”的优势是在哪里呢？
                </div>
                <div class="mt16">
                    任：首先“达芬奇”手术时看到的是3D的，可以将看到的东西放大10到15倍，用裸眼就可以看到更加清楚和准确，手术的时候就更加的精确。其次就是操作手腕比人的手腕更加的灵活，不仅能自由旋转540度，还能在手腕向下弯曲90度后，再自由旋转540度，所以可操作性也更强。最后就是在狭小的空间内能够进行切割和缝合等高难度的动作。因为前列腺手术的解剖结构特别深，所以相对而言手术难度比较大。用“达芬奇”机器人进行手术的时候，就可以突破前列腺癌手术的4个难点。
                </div>
                <div class="mt26">
                    <span class="color-red3">难点一:</span>
